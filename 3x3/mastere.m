function mastere(src,evnt)
if length(evnt.Key)>=1
    switch evnt.Key
    case 'f'
        twofront
        case 'g'
            twofrontp
        case 'r'
            tworight
        case 't'
            tworightp
        case 'b'
            twoback
        case'n'
            twobackp
        case 'h'
            twoscramble
        case 'u'
            twotop
        case 'y'
            twotopp
        case 'd'
            twobottom
        case 's'
            twobottomp
        case 'l'
            twoleft
        case 'k'
            twoleftp
    end
end


     
